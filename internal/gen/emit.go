package main

import (
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"
)

// emit writes the generated commands file, gofmt-formatted.
func emit(path string, gens []*genOp) error {
	var b strings.Builder
	b.WriteString("// Code generated by internal/gen; DO NOT EDIT.\n\n")
	b.WriteString("package generated\n\n")
	b.WriteString("import (\n")
	b.WriteString("\t\"context\"\n")
	b.WriteString("\t\"encoding/json\"\n\n")
	b.WriteString("\tgr4vygo \"github.com/gr4vy/gr4vy-go\"\n")
	b.WriteString("\t\"github.com/gr4vy/gr4vy-go/models/components\"\n")
	b.WriteString("\t\"github.com/gr4vy/gr4vy-go/models/operations\"\n\n")
	b.WriteString("\t\"github.com/gr4vy/gr4vy-cli/internal/commands\"\n")
	b.WriteString(")\n\n")

	// Reference imports so the file compiles even in the unlikely event a
	// generated body doesn't touch one of them.
	b.WriteString("var (\n")
	b.WriteString("\t_ = json.Marshal\n")
	b.WriteString("\t_ = components.Buyer{}\n")
	b.WriteString("\t_ = operations.ListBuyersRequest{}\n")
	b.WriteString(")\n\n")

	b.WriteString("func init() {\n")
	for _, g := range gens {
		writeOp(&b, g)
	}
	b.WriteString("}\n")

	formatted, err := format.Source([]byte(b.String()))
	if err != nil {
		_ = os.MkdirAll(filepath.Dir(path), 0o755)
		_ = os.WriteFile(path+".unformatted", []byte(b.String()), 0o644)
		return fmt.Errorf("format generated code: %w (wrote %s.unformatted for inspection)", err, path)
	}
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return err
	}
	return os.WriteFile(path, formatted, 0o644)
}

func writeOp(b *strings.Builder, g *genOp) {
	b.WriteString("commands.Register(&commands.Operation{\n")
	fmt.Fprintf(b, "Group: %q,\n", g.Group)
	fmt.Fprintf(b, "Name: %q,\n", g.Name)
	fmt.Fprintf(b, "Short: %q,\n", g.Short)
	if g.Long != "" {
		fmt.Fprintf(b, "Long: %q,\n", g.Long)
	}
	if len(g.PathParams) > 0 {
		b.WriteString("PathParams: []string{")
		for i, p := range g.PathParams {
			if i > 0 {
				b.WriteString(", ")
			}
			fmt.Fprintf(b, "%q", p)
		}
		b.WriteString("},\n")
	}
	if g.HasBody {
		b.WriteString("HasBody: true,\n")
		fmt.Fprintf(b, "BodyType: %q,\n", g.BodyType)
	}
	if g.IsList {
		b.WriteString("IsList: true,\n")
	}
	if len(g.Optionals) > 0 {
		b.WriteString("Optionals: []commands.Flag{\n")
		for _, f := range g.Optionals {
			fmt.Fprintf(b, "{Name: %q, Usage: %q, Kind: commands.%s},\n", f.Name, f.Usage, f.Kind)
		}
		b.WriteString("},\n")
	}
	b.WriteString("Run: func(ctx context.Context, c *gr4vygo.Gr4vy, in commands.Inputs) (any, error) {\n")
	b.WriteString(g.Body)
	b.WriteString("\n},\n")
	b.WriteString("})\n")
}
